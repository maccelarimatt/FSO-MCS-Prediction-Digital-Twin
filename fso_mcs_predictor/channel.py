def print_regime_summary(regime_name):
    """Print turbulence parameters for a regime (for verification)."""
    regime = config.TURBULENCE_REGIMES[regime_name]
    params = compute_turbulence_params(regime["Cn2"], regime["wind_mean"])

    print(f"\n{'='*50}")
    print(f"Regime: {regime_name}")
    print(f"  Cn2           = {regime['Cn2']:.1e} m^(-2/3)")
    print(f"  Wind          = {regime['wind_mean']} +/- {regime['wind_std']} m/s")
    print(f"  Rytov s2_R    = {params['sigma2_R']:.4f}")
    print(f"  VK s2_R       = {params['sigma2_R_VK']:.4f}")
    print(f"  r0            = {params['r0']*100:.2f} cm")
    print(f"  tau0          = {params['tau0']*1000:.3f} ms")
    print(f"  f_G           = {params['f_G']:.1f} Hz")
    print(f"  alpha (large) = {params['alpha']:.2f}")
    print(f"  beta  (small) = {params['beta']:.2f}")
    print(f"  Aperture avg  = {params['aperture_avg']:.4f}")
    print(f"{'='*50}")
