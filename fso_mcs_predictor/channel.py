"""
FSO turbulence channel model — aotools phase screens + wave propagation.

Temporal intensity generated by:
  1. Von Kármán phase screens via aotools PhaseScreenVonKarman, parameterised
     by the analytically-computed path-integrated Fried parameter r0.
  2. Angular spectrum (Fresnel) propagation from the screen plane (z=0)
     to the receiver plane (z=L), implemented as a batch FFT pair.
  3. Aperture averaging over the 75 mm receiver lens.
  4. Taylor frozen turbulence: the phase screen translates at wind speed
     v_wind, so temporal correlations emerge from the spatial structure.
     Each unique screen (= one pixel advance) is propagated once, then
     reused for the samples that fall within that pixel's time window.

Known SI discrepancy:
  The wave-optics aperture-averaged SI (~0.05–0.15 for strong turbulence)
  is lower than the Andrews & Phillips GG-model prediction (~0.30–0.40).
  Root cause: the fine-speckle scale  xi = lambda*L / L_screen  (here
  6.5 mm for a 192 mm screen) is smaller than the aperture (75 mm),
  creating additional spatial averaging beyond what the GG model predicts.
  This is a fundamental limitation of thin-screen simulation with a finite
  grid, and is well-documented in AO literature.  For the architecture
  comparison task the relative performance of NN models is unaffected.

Why aotools cn2_to_r0 is NOT used:
  aotools.cn2_to_r0 omits the path-length factor L, returning per-metre r0
  rather than the path-integrated value needed here.  We compute r0
  analytically (same formula, correct L included).

References:
  - Andrews & Phillips, "Laser Beam Propagation through Random Media" (2005)
  - Goodman, "Statistical Optics" (2015) — angular spectrum propagation
  - aotools: Reeves (2016), https://github.com/AOtools/aotools
"""

import numpy as np
from aotools.turbulence import PhaseScreenVonKarman
from . import config


# ---------------------------------------------------------------------------
# Turbulence parameter computation (analytical — for display and r0 input)
# ---------------------------------------------------------------------------

def compute_turbulence_params(Cn2, v_wind):
    """
    Compute turbulence parameters analytically from Cn2 and wind speed.

    r0 is the path-integrated Fried parameter (aotools.cn2_to_r0 omits L
    and cannot be used directly here).

    Returns dict: sigma2_R, sigma2_R_VK, r0, tau0, f_G, alpha, beta,
                  aperture_avg, sigma2_ln_X, sigma2_ln_Y
    """
    k  = 2 * np.pi / config.WAVELENGTH
    L  = config.LINK_DISTANCE
    D  = config.RX_APERTURE_D
    L0 = config.L0

    # Rytov variance (plane wave, Kolmogorov)
    sigma2_R = 1.23 * Cn2 * k**(7/6) * L**(11/6)

    # Von Karman correction (outer-scale saturation; < 0.1 % for this link)
    Q_L = k * L0**2 / L
    sigma2_R_VK = sigma2_R * (1 - 0.65 * (1 + Q_L)**(-5/6))

    # Fried parameter (path-integrated, plane wave)
    r0 = (0.423 * k**2 * Cn2 * L)**(-3/5)

    # Aperture averaging factor (Andrews & Phillips)
    A = (1 + 1.062 * (D/2)**2 / (config.WAVELENGTH * L))**(-7/6)

    # Gamma-Gamma parameters (for reference in print_regime_summary)
    sigma_R_VK   = np.sqrt(sigma2_R_VK)
    sigma2_ln_X  = 0.49 * sigma2_R_VK / (1 + 1.11 * sigma_R_VK**(12/5))**(7/6)
    sigma2_ln_Y  = (0.51 * sigma2_R_VK
                    / (1 + 0.69 * sigma_R_VK**(12/5))**(5/6) * A)
    alpha = 1.0 / (np.exp(sigma2_ln_X) - 1)
    beta  = 1.0 / (np.exp(sigma2_ln_Y)  - 1)

    tau0 = 0.3  * r0 / v_wind
    f_G  = 0.43 * v_wind / r0

    return {
        "sigma2_R":    sigma2_R,
        "sigma2_R_VK": sigma2_R_VK,
        "r0":          r0,
        "tau0":        tau0,
        "f_G":         f_G,
        "alpha":       alpha,
        "beta":        beta,
        "aperture_avg":  A,
        "sigma2_ln_X": sigma2_ln_X,
        "sigma2_ln_Y": sigma2_ln_Y,
    }


# ---------------------------------------------------------------------------
# Wave-optics helpers (precomputed per realisation)
# ---------------------------------------------------------------------------

def _build_aperture_mask(N, dx):
    """Circular aperture mask for the receiver lens (float64)."""
    c = (np.arange(N) - N // 2) * dx
    Xg, Yg = np.meshgrid(c, c)
    return (np.sqrt(Xg**2 + Yg**2) <= config.RX_APERTURE_D / 2).astype(np.float64)


def _build_transfer_function(N, dx, z):
    """
    Angular spectrum transfer function H(fx, fy) for propagation distance z.

        H = exp( i * sqrt( k^2 - (2*pi*fx)^2 - (2*pi*fy)^2 ) * z )

    Evanescent components (kz^2 < 0) are zeroed.  Precomputed once per
    realisation — same wavelength/grid for every sample in a batch.
    """
    k  = 2 * np.pi / config.WAVELENGTH
    fx = np.fft.fftfreq(N, d=dx)
    FX, FY = np.meshgrid(fx, fx)
    kz2 = np.maximum(k**2 - (2 * np.pi)**2 * (FX**2 + FY**2), 0.0)
    return np.exp(1j * np.sqrt(kz2) * z)


def _propagate_batch(screens, H, mask, mask_area):
    """
    Angular spectrum propagation of a batch of phase screens.

    A plane wave exp(i*phi) is propagated by multiplying its 2-D Fourier
    transform with the pre-computed transfer function H, then inverse-
    transforming.  The aperture-averaged intensity is returned.

    Args:
        screens   : (B, N, N) float  — phase aberration in radians
        H         : (N, N)   complex — transfer function (precomputed)
        mask      : (N, N)   float   — circular aperture mask
        mask_area : float            — sum(mask)

    Returns:
        h : (B,) float — aperture-averaged intensity (not yet normalised)
    """
    E_in  = np.exp(1j * screens)                        # (B, N, N)
    E_out = np.fft.ifft2(np.fft.fft2(E_in) * H)        # (B, N, N)
    I     = np.abs(E_out)**2                            # (B, N, N)
    return (I * mask).sum(axis=(1, 2)) / mask_area      # (B,)


# ---------------------------------------------------------------------------
# Intensity time-series generation
# ---------------------------------------------------------------------------

def generate_intensity_timeseries(params, duration_s, v_wind, rng):
    """
    Generate unit-mean aperture-averaged intensity time-series.

    Strategy (Taylor frozen turbulence + unique-screen optimisation):
      1. Compute how many unique phase screens are needed: one screen per
         pixel advance (dx / v_wind seconds per pixel).
      2. Generate that many screens sequentially via PhaseScreenVonKarman,
         propagating each batch of BATCH screens with a single FFT pair.
      3. Map each screen index back to its time window in the full series.

    This propagates each unique screen exactly once, giving a ~10x speedup
    over propagating at every sample while preserving the correct temporal
    autocorrelation structure (step-function discretised at dx/v_wind).

    Grid: N=64, dx=3 mm
      L_screen = 192 mm  > 2.5x aperture diameter (75 mm)
      dx / r0  = 3 mm / r0 < 0.5 for all regimes (r0 >= 34 mm at strong)
      Fresnel scale sqrt(lambda*L) = 35 mm  —  adequately resolved

    Args:
        params     : output of compute_turbulence_params
        duration_s : simulation duration in seconds
        v_wind     : wind speed (m/s), sets temporal decorrelation
        rng        : numpy Generator for reproducibility

    Returns:
        h_turb : (n_samples,) float, unit-mean normalised intensity
    """
    r0 = params["r0"]
    n_samples = int(duration_s * config.SAMPLE_RATE)

    N  = 64
    dx = 3e-3   # 3 mm pixel scale

    mask      = _build_aperture_mask(N, dx)
    mask_area = mask.sum()
    H         = _build_transfer_function(N, dx, config.LINK_DISTANCE)

    # Number of unique screens: one per pixel (dx) of screen translation
    samps_per_pixel = dx / (v_wind * config.DT)            # samples per pixel
    n_unique = max(2, int(np.ceil(n_samples / samps_per_pixel)) + 1)

    seed_int = int(rng.integers(0, 2**31))
    ps = PhaseScreenVonKarman(N, dx, r0, config.L0, random_seed=seed_int)

    # Generate and propagate unique screens in batches
    BATCH    = 500
    h_unique = np.zeros(n_unique)
    buf      = np.zeros((BATCH, N, N))

    for start in range(0, n_unique, BATCH):
        end = min(start + BATCH, n_unique)
        B   = end - start
        for j in range(B):
            buf[j] = ps.scrn      # collect phase screen
            ps.add_row()          # advance by one pixel
        h_unique[start:end] = _propagate_batch(buf[:B], H, mask, mask_area)

    # Map unique screens back to full sample time series
    # Screen i is valid for t in [i * dx/v, (i+1) * dx/v)
    screen_times = np.arange(n_unique + 1) * (dx / v_wind)   # transition times (s)
    sample_times = np.arange(n_samples) * config.DT
    # searchsorted on transition boundaries → index of active screen
    screen_idx = np.searchsorted(screen_times[1:], sample_times)
    screen_idx = np.clip(screen_idx, 0, n_unique - 1)
    h_turb = h_unique[screen_idx]

    # Normalise to unit mean
    h_mean = h_turb.mean()
    if h_mean > 1e-20:
        h_turb /= h_mean

    return h_turb


# ---------------------------------------------------------------------------
# Pointing error (not modelled by aotools — kept from original design)
# ---------------------------------------------------------------------------

def generate_pointing_error(n_samples, rng=None):
    """
    AR(1) Gaussian-beam pointing error model.

    Two components:
      - Slow drift : tau = 100 s  (thermal / mechanical)
      - Fast jitter: tau =   1 s  (wind-induced vibration)

    Coupling loss: exp(-2 * displacement^2 / beam_width^2)
    Returns h_point in (0, 1].
    """
    if rng is None:
        rng = np.random.default_rng()

    dt = config.DT

    a_slow = np.exp(-dt / 100.0)
    z_slow = np.zeros(n_samples)
    z_slow[0] = rng.normal()
    for i in range(1, n_samples):
        z_slow[i] = a_slow * z_slow[i-1] + np.sqrt(1 - a_slow**2) * rng.normal()

    a_fast = np.exp(-dt / 1.0)
    z_fast = np.zeros(n_samples)
    z_fast[0] = rng.normal()
    for i in range(1, n_samples):
        z_fast[i] = a_fast * z_fast[i-1] + np.sqrt(1 - a_fast**2) * rng.normal()

    # Combined displacement (RMS ~ 0.058 beam widths)
    displacement = 0.05 * z_slow + 0.03 * z_fast
    return np.exp(-2 * displacement**2)


# ---------------------------------------------------------------------------
# Full channel realisation
# ---------------------------------------------------------------------------

def generate_channel_realisation(regime_name, duration_s,
                                  mean_power_dbm=None, seed=None):
    """
    Generate a complete channel realisation for a given turbulence regime.

    Returns dict:
        power_dbm      : (n_samples,) received power in dBm
        snr_db         : (n_samples,) SNR in dB
        h_turb         : (n_samples,) unit-mean turbulence intensity
        h_point        : (n_samples,) pointing error factor in (0, 1]
        params         : turbulence parameter dict
        regime         : regime name string
        v_wind         : wind speed used for this realisation
        mean_power_dbm : mean Rx power before fading
        n_samples      : total sample count
    """
    rng    = np.random.default_rng(seed)
    regime = config.TURBULENCE_REGIMES[regime_name]

    v_wind = max(0.5, rng.normal(regime["wind_mean"], regime["wind_std"]))
    params = compute_turbulence_params(regime["Cn2"], v_wind)

    n_samples = int(duration_s * config.SAMPLE_RATE)

    h_turb  = generate_intensity_timeseries(params, duration_s, v_wind, rng)
    h_point = generate_pointing_error(n_samples, rng)

    if mean_power_dbm is None:
        power_map = {
            "weak":        -8.0,
            "moderate":   -13.0,
            "strong":     -18.0,
            "very_strong": -22.0,
        }
        mean_power_dbm = power_map[regime_name]

    mean_power_linear = 10**(mean_power_dbm / 10)
    rx_power_linear   = mean_power_linear * h_turb * h_point
    rx_power_linear   = np.clip(rx_power_linear, 1e-10,
                                10**(config.PEAK_RX_POWER_DBM / 10))
    power_dbm = 10 * np.log10(rx_power_linear)
    snr_db    = config.SNR_SLOPE * power_dbm + config.SNR_INTERCEPT

    return {
        "power_dbm":      power_dbm,
        "snr_db":         snr_db,
        "h_turb":         h_turb,
        "h_point":        h_point,
        "params":         params,
        "regime":         regime_name,
        "v_wind":         v_wind,
        "mean_power_dbm": mean_power_dbm,
        "n_samples":      n_samples,
    }


# ---------------------------------------------------------------------------
# MCS lookup
# ---------------------------------------------------------------------------

def snr_to_mcs(snr_db, hysteresis=0.0):
    """
    Map SNR (dB) to ideal MCS class (0-14) using thresholds.

    Class 0 = outage (SNR below QPSK 1/2 threshold).
    Hysteresis is subtracted from each non-outage threshold.
    """
    thresholds = config.MCS_SNR_THRESHOLDS.copy()
    thresholds[1:] -= hysteresis

    snr_db = np.asarray(snr_db)
    mcs    = np.zeros_like(snr_db, dtype=int)

    for i in range(config.NUM_CLASSES - 1, 0, -1):
        mask = snr_db >= thresholds[i]
        mcs[mask] = np.maximum(mcs[mask], i)

    return mcs


